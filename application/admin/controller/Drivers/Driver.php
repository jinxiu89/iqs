namespace app\admin\controller\Drivers;

use think\Request;
use app\common\models\Drivers as DriversModel;
use think\Route;
use app\admin\controller\Base;
/**
 * Class Index
 * @package app\admin\controller
 */
class Driver extends Base
{
    public function initialize()
    {
        parent::initialize(); // TODO: Change the autogenerated stub
    }

    /**
     * @return mixed
     */
    public function index()
    {
        $data = (new DriversModel())->getDrivers();
        $count = $data->count();
        return $this->fetch('',[
            'data' => $data,
            'count' => $count
        ]);
    }

    /**
     * @return mixed
     */
    public function add()
    {
        if (Request()->isGet()) {
            return $this->fetch('', [
                'to_level' => $this->toLevel,
            ]);
        }
        if (Request()->isPost()) {
            $data = input('post.');
            return (new DriversModel())->saveData($data);
        }
    }
    public function edit($id){
        if(Request()->isGet()){
            $data=(new DriversModel())->getDataById($id);
            return $this->fetch('',[
                'to_level'=>$this->toLevel,
                'data' =>$data
            ]);
        }
        if(Request()->isPost()){
            $data= input('post.');
            return (new DriversModel())->saveData($data);
        }
    }
}